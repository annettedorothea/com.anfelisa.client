import AbstractErrorEvent from "../../../gen/common/events/AbstractErrorEvent";

export default class ErrorEvent extends AbstractErrorEvent {
    prepareDataForView() {
        this.eventData = JSON.parse(JSON.stringify(this.eventParam));
        if (this.eventData.data === undefined) {
        	this.eventData.data = {};
        }
    }
}

