import AbstractMessageEvent from "../../../gen/common/events/AbstractMessageEvent";

export default class MessageEvent extends AbstractMessageEvent {
    prepareDataForView() {
        this.eventData = JSON.parse(JSON.stringify(this.eventParam));
        if (this.eventData.data === undefined) {
        	this.eventData.data = {};
        }
    }
}

